#include <iostream>
using namespace std;
int main()
{
    int k, S = 0;
    cin >> k;
    
    for (int i = 0; i < k ; i++)
    {
        S += (i+1);
    }
    
    cout << S;

    return 0;
}

//می‌دانیم تلفن مرد مالیاتچی برای اینکه از «آی» درصد شارژ 
